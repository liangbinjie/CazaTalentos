    public void agregarFact(){
        Facturacioon a = new Facturacioon();
        a.setNombrefa(jTextPane1.getText());
        a.setFecha(jTextPane2.getText());
        a.setHora(jTextPane3.getText());
        try {
            DataOutputStream salida = new DataOutputStream(new FileOutputStream("Facturas.dat", true));
            salida.writeUTF(a.getNombrefa());
            salida.writeUTF(a.getHora());
            salida.writeUTF(a.getFecha());
            // DEBEN ENTRAR LOS NOMBRES DEL DEPORTISTA
            JOptionPane.showMessageDialog(null, "¡FACTURA REALIZADA!",
                    "Datos agregados", JOptionPane.INFORMATION_MESSAGE);
            limpiar();
            salida.close();
        } catch(IOException ex01){
            JOptionPane.showMessageDialog(null, "¡ERROR AL REALIZAR LA FACTURA, INTENTE DE NUEVO!", "Error",
                    JOptionPane.ERROR_MESSAGE);
        }
    }
    public void limpiar(){
        jTextPane1.setText("");
        jTextPane2.setText("");
        jTextPane3.setText("");
        jTextPane4.setText(""); 
        jTextPane1.requestFocus();
    }
    
    
