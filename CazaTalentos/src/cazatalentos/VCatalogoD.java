    public void limpiar() {
        nameField.setText("");
        surnameField.setText("");
        ageField.setText("");
        idField.setText("");
        phoneField.setText("");
        cityField.setText("");
        addressField.setText("");
        emailField.setText("");   
    }
    
    public void guardar() {
        try {
            int edad = Integer.parseInt(ageField.getText());
            long identificacion = Long.parseLong(idField.getText());
            long telefono = Long.parseLong(phoneField.getText());
            Deportistas d = new Deportistas();
            d.setNombre(nameField.getText());
            d.setApellidos(surnameField.getText());
            d.setEdad(edad);
            d.setIdentificacion(identificacion);
            d.setTelefono(telefono);
            d.setDireccion(addressField.getText());
            d.setCiudad(cityField.getText());
            d.setEmail(emailField.getText());
            Main.deportistas.add(d);
            JOptionPane.showMessageDialog(null, "Informacion guardada");
            limpiar();
            bloquear();
            
        } catch (HeadlessException | NumberFormatException ex) {
            JOptionPane.showMessageDialog(null, "Ocurrio un error al guardar la informacion, verifique que la informacion este correcta");
        }

    }
    
    public void bloquear() {
        nameField.setEditable(false);
        surnameField.setEditable(false);
        idField.setEditable(false);
        ageField.setEditable(false);
        phoneField.setEditable(false);
        addressField.setEditable(false);
        cityField.setEditable(false);
        emailField.setEditable(false);
    }
    
    public boolean search(long identificacion) {
        boolean existe = false;
        Deportistas dp = new Deportistas();
        for (Deportistas d: Main.deportistas) {
            if (d.getIdentificacion() == identificacion) {
                dp = d;
                existe = true;
            }
        }
        
        if (existe) {
            nameField.setText(dp.getNombre());
            surnameField.setText(dp.getApellidos());
            ageField.setText(String.valueOf(dp.getEdad()));
            idField.setText(String.valueOf(dp.getIdentificacion()));
            phoneField.setText(String.valueOf(dp.getTelefono()));
            cityField.setText(dp.getCiudad());
            addressField.setText(dp.getDireccion());
            emailField.setText(dp.getEmail());   
        } 
        
        return existe;
    }
    
    public void modificar() {
        if (!searchField.getText().isEmpty()) {
            if (search(Long.parseLong(searchField.getText()))) {
                eliminar(Long.parseLong(searchField.getText()));
            }
        }
        nameField.setEditable(true);
        surnameField.setEditable(true);
        idField.setEditable(true);
        ageField.setEditable(true);
        phoneField.setEditable(true);
        addressField.setEditable(true);
        cityField.setEditable(true);
        emailField.setEditable(true);
    }

    
    
    public boolean eliminar(long id) {
        boolean eliminado = false;
        for (int i=0; i<Main.deportistas.size(); i++) {
            if (Main.deportistas.get(i).getIdentificacion() == id) {
                Main.deportistas.remove(i);
                eliminado = true;
            }
        }
        
        return eliminado;
    }
    

