    private void searchBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchBtnActionPerformed
        // TODO add your handling code here:
        limpiar();
        if (!searchField.getText().isEmpty()) {
            boolean existe = search(Long.parseLong(searchField.getText()));
            if (!existe) {
                JOptionPane.showMessageDialog(null, "Deportista no existe");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Por favor ingrese una identificacion");
        }
    }//GEN-LAST:event_searchBtnActionPerformed

    private void deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteActionPerformed
        // TODO add your handling code here:
        if (searchField.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Por favor ingrese una identificacion para eliminar");
        } else {
            if (eliminar(Long.parseLong(searchField.getText()))) {
                JOptionPane.showMessageDialog(null, "Deportistas eliminado");
            } else {
                JOptionPane.showMessageDialog(null, "Deportista no encontrado");
            }
        }
    }//GEN-LAST:event_deleteActionPerformed

